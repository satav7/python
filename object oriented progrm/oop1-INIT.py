class students:
    def __init__(self, name, grade, birthdate):
        self.name = name
        self.grade = grade
        self.birthdate = birthdate

khushi = students('khushi', 8, 1998)
joy = students('joy', 8, 1998)


print(khushi.name)
print(joy.name)

print(khushi.grade)
print(joy.grade)

print(khushi.birthdate)
print(joy.birthdate)
